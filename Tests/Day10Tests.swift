import Testing
import AoCTools
@testable import AdventOfCode

@Suite struct Day10Tests {
    @Test func testDay10_part1() throws {
        let d1 = Day10(input: """
            .#..#
            .....
            #####
            ....#
            ...##
            """)
        #expect(d1.part1() == 8)

        let d2 = Day10(input: """
            ......#.#.
            #..#.#....
            ..#######.
            .#.#.###..
            .#..#.....
            ..#....#.#
            #..#....#.
            .##.#..###
            ##...#..#.
            .#....####
            """)
        #expect(d2.part1() == 33)

        let d3 = Day10(input: """
            #.#...#.#.
            .###....#.
            .#....#...
            ##.#.#.#.#
            ....#.#.#.
            .##..###.#
            ..#...##..
            ..##....##
            ......#...
            .####.###.
            """)
        #expect(d3.part1() == 35)

        let d4 = Day10(input: """
            .#..##.###...#######
            ##.############..##.
            .#.######.########.#
            .###.#######.####.#.
            #####.##.#.##.###.##
            ..#####..#.#########
            ####################
            #.####....###.#.#.##
            ##.#################
            #####.##.###..####..
            ..######..##.#######
            ####.##.####...##..#
            .#####..#.######.###
            ##...#.##########...
            #.##########.#######
            .####.#.###.###.#.##
            ....##.##.###..#####
            .#.#.###########.###
            #.#.#.#####.####.###
            ###.##.####.##.#..##
            """)
        #expect(d4.part1() == 210)
    }

    @Test func testDay10_part2() throws {
        let d3 = Day10(input: """
        .#....#####...#..
        ##...##.#####..##
        ##...#...#.#####.
        ..#.....#...###..
        ..#.#.....#....##
        """)
        #expect(d3.part2() == 0)

        let d4 = Day10(input: """
            .#..##.###...#######
            ##.############..##.
            .#.######.########.#
            .###.#######.####.#.
            #####.##.#.##.###.##
            ..#####..#.#########
            ####################
            #.####....###.#.#.##
            ##.#################
            #####.##.###..####..
            ..######..##.#######
            ####.##.####...##..#
            .#####..#.######.###
            ##...#.##########...
            #.##########.#######
            .####.#.###.###.#.##
            ....##.##.###..#####
            .#.#.###########.###
            #.#.#.#####.####.###
            ###.##.####.##.#..##
            """)
        #expect(d4.part2() == 802)
    }

    @Test func testDay10_part1_solution() throws {
        let day = Day10(input: Day10.input)
        #expect(day.part1() == 230)
    }

    @Test func testDay10_part2_solution() throws {
        let day = Day10(input: Day10.input)
        #expect(day.part2() == 1205)
    }
}
